a=int(input("enter a value :"))
b=int(input("enter b value :"))
choice=input("enter operation (+,-,*,/):")
if choice=="+":
    print(a+b)
elif choice=="-":
    print(a-b)
elif choice=="*":
    print(a*b)
elif choice=="/":
    print(a/b)
else:
    print("invalid operation")    
